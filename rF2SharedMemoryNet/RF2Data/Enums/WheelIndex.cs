namespace rF2SharedMemoryNet.RF2Data.Enums
{
    /// <summary>
    /// Represents the index of a wheel on a vehicle in rFactor 2.
    /// </summary>
    /// <remarks>This enumeration is used to identify specific wheels on a vehicle, such as the front-left or
    /// rear-right. The values correspond to the zero-based index of the wheel.</remarks>
    public enum WheelIndex
    {
        /// <summary>
        /// Front-left wheel
        /// </summary>
        FrontLeft,

        /// <summary>
        /// Front-right wheel
        /// </summary>
        FrontRight,

        /// <summary>
        /// Rear-left wheel
        /// </summary>
        RearLeft,

        /// <summary>
        /// Rear-right wheel
        /// </summary>
        RearRight
    }
}
