    /// <summary>
    /// Represents the severity levels of RF2 denting.
    /// </summary>
    /// <remarks>This enumeration is used to categorize the extent of denting in RF2 components. The values
    /// range from no denting to increasing levels of severity.</remarks>
    public enum DentSeverity
    {
        /// <summary>
        /// No denting
        /// </summary>
        None,
        /// <summary>
        /// Minor denting
        /// </summary>
        Some,
        /// <summary>
        /// Major denting
        /// </summary>
        More
    }
}